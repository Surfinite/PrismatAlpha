/**
 * Color string to int conversion.
 * From C.as:605-617.
 */
function lowercaseColorToInt(string) {
    if (string === 'white') return COLOR_WHITE;
    if (string === 'black') return COLOR_BLACK;
    ASSERT(false, 'Invalid color string.');
    return -1;
}

/**
 * ASSERT — fail-hard assertion (transpilation convention: throw, not silent ignore).
 * From C.as:619-622. In AS3 this routes to DebugTools.ASSERT.
 */
function ASSERT(expr, message) {
    if (!expr) {
        const err = new Error(`ASSERT failed: ${message || 'no message'}`);
        console.error(err.message);
        throw err;
    }
}

